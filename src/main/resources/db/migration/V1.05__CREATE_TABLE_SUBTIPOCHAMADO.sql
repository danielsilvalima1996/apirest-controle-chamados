CREATE TABLE IF NOT EXISTS subtipoChamado(
	id BIGINT AUTO_INCREMENT NOT NULL,
	descricao VARCHAR(50) NOT NULL,
	criado DATETIME NOT NULL,
	modificado DATETIME NOT NULL,
	ativo BIT(1) NOT NULL,
	idTipoChamado BIGINT NOT NULL,
	FOREIGN KEY (idTipoChamado) REFERENCES tipoChamado (id),
	PRIMARY KEY (id)
);